//=================================
void oled_Init(void)
{
	oled_WriteCmd(0x38);//function set
	oled_WriteCmd(0x38);//function set
	oled_WriteCmd(0x38);//function set
	oled_WriteCmd(0x38);//function set
	oled_WriteCmd(0x08);//display off
	oled_WriteCmd(0x06);//entry mode set
	oled_WriteCmd(0x1f);//Graphic mode and internel power on (have to turn on the internel power to get the best brightness)
	oled_WriteCmd(0x01);//clear display
	oled_WriteCmd(0x02);
	oled_WriteCmd(0x0c);//display on
	oled_Delay(OLED_DELAY_INIT);
}
	
//==================================
//   Clean
//==================================
void oled_cleanScreen(void)
{
	u8 y;

		for(y = 0;y<MAXCOL;y++){
				
				//First line address
				oled_WriteCmd(SETDDRAMADD1st(y));//X
				oled_WriteCmd(SELPAGEADD(0x01));//Y
				oled_WriteData(0xFF);
				
				//Second line address
				oled_WriteCmd(SETDDRAMADD1st(y));//X
				oled_WriteCmd(SELPAGEADD(0x00));//Y
				oled_WriteData(0xFF);
		}
}


void oled_DrawImg(const u8 *p)
{
	u8 i;
	
	oled_WriteCmd(SETDDRAMADD1st(0));//X
	oled_WriteCmd(SELPAGEADD(0x00));//Y			//Set display data RAM addr:40H
	
	for(i = 0; i<100;i++){
		oled_WriteData(*p);
		p++;
	}

	oled_WriteCmd(SETDDRAMADD1st(0));//X
	oled_WriteCmd(SELPAGEADD(0x01));//Y			//Set display data RAM addr:41H
	
	for(i =0; i<100;i++){
		oled_WriteData(*p);
		p++;
	}
	oled_Delay(20);
}





