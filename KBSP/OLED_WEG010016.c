//=================================
void oled_Delay(u32 del)
{
  u32 i;
  for( ; del!= 0 ; del--){
		i=11;
		for( ; i != 0 ; i--);
  }
}

//=================================
void oled_WriteCmd(u8 cmd)
{
	OLED_RS = 0;
	//OLED_CS = 0;
	OLED_RW = 0;
	OLED_DATA = cmd;
	OLED_E = 1;            //1us
	oled_Delay(OLED_DELAY_CK);
	OLED_E = 0;		//1us
	//OLED_CS = 1;
	//oled_CheckBusy();
	oled_Delay(LCD_DELAY_DATA);
}

//=================================
void oled_WriteData(u8 dat)
{
	OLED_RS = 1;
	//OLED_CS = 0;
	OLED_RW = 0;
	OLED_DATA = dat;
	OLED_E = 1;
	oled_Delay(OLED_DELAY_CK);
	OLED_E = 0;
	//OLED_CS = 1;
	//oled_CheckBusy(); 
	oled_Delay(LCD_DELAY_DATA);
}

//=================================
u8 oled_ReadData(void)
{
	u8 dat;
	
	OLED_RS = 1;
	//OLED_CS = 0;
	OLED_RW = 1;
	//OLED_DATA = 0xff; // input mode
	OLED_E = 1;
	oled_Delay(OLED_DELAY_CK);
	//oled_readData = OLED_DATA;
	OLED_E = 0;
	//OLED_CS = 1;
	//oled_CheckBusy(); 
	oled_Delay(LCD_DELAY_DATA);
	return dat;
}

